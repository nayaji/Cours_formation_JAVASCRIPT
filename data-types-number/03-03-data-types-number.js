// numberOperations.js

// Function 1
function addNumbers(num1, num2) {
  return num1 + num2;
}

// Function 2
function subtractNumbers(num1, num2) {
  return num1 - num2;
}

// Function 3
function multiplyNumbers(num1, num2) {
  return num1 * num2;
}

// Function 4
function divideNumbers(num1, num2) {
  return num1 / num2;
}

// Function 5
function powerOfNumber(base, exponent) {
  return base ** exponent;
}

module.exports = {
  addNumbers,
  subtractNumbers,
  multiplyNumbers,
  divideNumbers,
  powerOfNumber,
};
